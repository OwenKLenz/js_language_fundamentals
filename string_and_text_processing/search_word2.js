// Search Word Part 2

// Goal: Search a string of text for all instances of a word and highlight (**word**) those instances. Return the complete sentence with highlighted words

// Algorithm:
//   1. Find each word in the text
//     - Use the replace array method with a regex capturing instance of the word (create a new regex interpolating the word)
//   2. Replace the word with itself uppercased and enclosed in **s
//     - Use a callback function with replace that returns the captured word toUpperCased and enclosed in **s 

'use strict';

function searchWord(findWord, text) {
  if (!findWord || !text) return text;

  const highlightWord = word => '**' + word.toUpperCase() + '**';
  return text.replace(new RegExp(`\\b${findWord}\\b`, 'gi'), highlightWord);
}

